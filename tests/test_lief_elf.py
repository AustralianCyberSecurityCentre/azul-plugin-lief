class TestExecute(test_template.TestPlugin):
    """Parent class of all Lief ELF tests."""

    PLUGIN_TO_TEST = LiefELF

    def test_lief_elf_pe(self):
        """Tests on a PE file."""
        with self.assertRaises(AssertionError):
            self.do_execution(
                data_in=[
                    (
                        DataLabel.CONTENT,
                        self.load_test_file_bytes(
                            "702e31ed1537c279459a255460f12f0f2863f973e121cd9194957f4f3e7b0994",
                            "Benign WIN32 EXE, python library executable python_mcp.exe",
                        ),
                    )
                ]
            )

    def test_lief_elf_bad_elf(self):
        """Tests on a bad ELF file."""
        result = self.do_execution(
            data_in=[
                (
                    DataLabel.CONTENT,
                    self.load_cart(
                        "8efa1dc285627d92c20dc840e0a53544cfe64098af6608772c06c7c5734057c0.cart",
                        description="Malformed benign ELF file created by Azul team.",
                    ),
                )
            ],
            verify_input_content=False,
        )
        self.assertJobResult(
            result,
            JobResult(
                state=State(State.Label.COMPLETED),
                events=[
                    Event(
                        entity_type="binary",
                        entity_id="8efa1dc285627d92c20dc840e0a53544cfe64098af6608772c06c7c5734057c0",
                        features={"tag": [FV("elf_invalid")]},
                    )
                ],
            ),
        )

